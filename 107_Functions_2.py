# More functions!

# define a add, subtract, divide and multiply function
def Add(val1, val2):
    return val1 + val2


def Subtract(val1, val2):
    return val1 - val2


def Multiply (val1, val2):
    return val1 * val2


def Divide(val1, val2):
    return val1 / val2

print (Add(10,9))
print(Subtract(5,3))
print(Multiply(5,5))
print(Divide(10, 5))